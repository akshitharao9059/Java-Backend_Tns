package com.tnsif.intro.java.student;

public class student {

	    private int sid;
	    private String sname;
	    private int marks;

	    // Getter for sid
	    public int getSid() {
	        return sid;
	    }

	    // Setter for sid
	    public void setSid(int sid) {
	        this.sid = sid;
	    }

	    // Getter for sname
	    public String getSname() {
	        return sname;
	    }

	    // Setter for sname
	    public void setSname(String sname) {
	        this.sname = sname;
	    }

	    // Getter for marks
	    public int getMarks() {
	        return marks;
	    }

	    // Setter for marks
	    public void setMarks(int marks) {
	        this.marks = marks;
	    }
	}	